SUB = str.maketrans('0123456789', '₀₁₂₃₄₅₆₇₈₉')
# -------------------------------------------------------------------------------------------------



# -------------------------------------------------------------------------------------------------
def sub(s):
	if isinstance(s, int):
		s = str(s)

	return s.translate(SUB)
