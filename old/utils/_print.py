def print(*args, **kwargs):
    if 'prefix' in kwargs.keys():
        __builtin__.Print(kwargs['prefix'], end='')
        del kwargs['prefix']

    if 'color' in kwargs.keys():
        cprint(*args, **kwargs)
    else:
        __builtin__.Print(*args, **kwargs)

